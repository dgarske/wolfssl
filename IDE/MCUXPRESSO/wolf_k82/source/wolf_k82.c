uint32_t rsa_sigver(const unsigned char* key, unsigned int keysz,
		const unsigned char* msg, unsigned int msgsz,
		const unsigned char* sig, unsigned int sigsz) {
    uint32_t err = 0;
    word32 idx = 0;
    RsaKey rsakey;
    uint8_t verify[256];
    word32 verifySz = (word32)sizeof(verify);

    memset(verify, 0, sizeof(verify));

    if (err == 0 && wc_InitRsaKey(&rsakey, NULL)) {
        err = -1;
    }

    if (err == 0 && wc_RsaPublicKeyDecode(key, &idx, &rsakey, keysz)) {
        err = -1;
    }

    if (err == 0 && (verifySz = wc_RsaSSL_Verify(sig, sigsz, verify, verifySz, &rsakey)) < 0) {
        err = -1;
    }

    if (msgsz != verifySz || XMEMCMP(msg, verify, verifySz) != 0) {
    	err = -1;
    }

    wc_FreeRsaKey(&rsakey);

    return err;
}

uint32_t test_rsa_sigver(void)
{
    if (rsa_sigver(valid_key, sizeof(valid_key), valid_msg, sizeof(valid_msg), valid_sig, sizeof(valid_sig)) != 0) {
        return -1;
    }
    if (rsa_sigver(invalid_key, sizeof(invalid_key), invalid_msg, sizeof(invalid_msg), invalid_sig, sizeof(invalid_sig)) == 0) {
        return -1;
    }
    return 0;
}


